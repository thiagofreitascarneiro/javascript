function calculadora (valor1, valor2) {
    const soma = valor1 + valor2
    console.log('A soma dos valores são:', soma);
    const divisao = valor1 / valor2
    console.log('A divisao dos valores são:', divisao);
    const subtracao = valor1 - valor2
    console.log('A subtração dos valores são:', subtracao);
    const multiplica = valor1 * valor2
    console.log('A multiplicação dos valores são:', multiplica);

}


calculadora(2, 2)

